# Append Method
list1 = ["Sai",3,2.2,True]
list1.append(False)
print(list1)

# Insert Method
list1.insert(1,4)
print(list1)

# Remove Method
list1.remove(True)
print(list1)

# Pop Method
list1.pop()
print(list1)

# Reverse Method
list1.reverse()
print(list1)

# Sort Method
list1.remove("Sai")
list1.sort()
print(list1)

# Extend Method
list2 = [10,24,56,37.8,90]
list1.extend(list2)
print(list1)
